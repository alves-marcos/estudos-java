package util;

public class Calculadora {

    public static double sum(double a, double b){
        return a + b;
    }

    public static double sub(double a, double b){
        return a - b;
    }

    public static double mult(double a, double u){
        return a * u;
    }

    public static double div(double a, double b){
        return a/b;
    }


}
